import React, { useEffect, useState } from "react";
import styles from "./Checkout.module.scss";
import { Button } from "../../components/ui/Buttons/Button";
import { useAuthStore } from "../../store";
import { Link } from "react-router-dom";
import { useCartStore } from "../../store/useCartStore";
import { orderService } from "../../services/OrderService";
import type { CheckoutFormData } from "./CheckoutForm";
import CheckoutForm from "./CheckoutForm";

const Checkout: React.FC = () => {
  const { user } = useAuthStore();
  const { cart, setCart, clearCart } = useCartStore();
  const [loading, setLoading] = useState(false);
  const [paymentMethod, setPaymentMethod] = useState("cod");

  const [form, setForm] = useState<CheckoutFormData>({
    fullName: user?.first_name || "",
    phone: "",
    email: user?.email || "",
    city: "",
    novaPoshtaBranch: ""
  });

  useEffect(() => {
    const storedCart = localStorage.getItem("cart");
    if (storedCart) {
      try {
        setCart(JSON.parse(storedCart));
      } catch {
        setCart([]);
      }
    }
  }, [setCart]);

  const handleOrder = async () => {
    if (cart.length === 0) {
      alert("üõí –í–∞—à –∫–æ—à–∏–∫ –ø–æ—Ä–æ–∂–Ω—ñ–π");
      return;
    }

    if (!form.fullName || !form.phone || !form.city || !form.novaPoshtaBranch) {
      alert("‚ö† –ó–∞–ø–æ–≤–Ω—ñ—Ç—å —É—Å—ñ –æ–±–æ–≤‚Äô—è–∑–∫–æ–≤—ñ –ø–æ–ª—è");
      return;
    }

    setLoading(true);
    try {
      if (user) {
        await orderService.createOrder(user?.id ?? null, cart, paymentMethod, form);
      }

      await orderService.sendTelegramOrder(
        {
          ...(user ? { id: user.id, email: user.email } : { guest: true }),
          ...form
        },
        cart,
        paymentMethod
      );

      localStorage.removeItem("cart");
      clearCart();
      alert("‚úÖ –ó–∞–º–æ–≤–ª–µ–Ω–Ω—è –≤—ñ–¥–ø—Ä–∞–≤–ª–µ–Ω–æ!");
    } catch (err) {
      console.error(err);
      alert("‚ùå –°—Ç–∞–ª–∞—Å—è –ø–æ–º–∏–ª–∫–∞ –ø—Ä–∏ –æ—Ñ–æ—Ä–º–ª–µ–Ω–Ω—ñ –∑–∞–º–æ–≤–ª–µ–Ω–Ω—è");
    } finally {
      setLoading(false);
    }
  };

  const total = cart.reduce((sum, item) => {
    const price =
      item.priceSale && item.priceSale < item.price ? item.priceSale : item.price;
    return sum + price * item.quantity;
  }, 0);

  return (
    <div className={styles.checkout}>
      <h1>–û—Ñ–æ—Ä–º–ª–µ–Ω–Ω—è –∑–∞–º–æ–≤–ª–µ–Ω–Ω—è</h1>

      <CheckoutForm form={form} setForm={setForm} />

      <div className={styles.summary}>
        {cart.map(item => (
          <div key={item.productId} className={styles.item}>
            <span>{item.name} x {item.quantity}</span>
            <span>
              ‚Ç¥{(
                (item.priceSale && item.priceSale < item.price ? item.priceSale : item.price) *
                item.quantity
              ).toFixed(2)}
            </span>
          </div>
        ))}
        <div className={styles.total}>
          <strong>–ó–∞–≥–∞–ª—å–Ω–∞ —Å—É–º–∞:</strong> ‚Ç¥{total.toFixed(2)}
        </div>
      </div>

      <div className={styles.payment}>
        <h2>–ú–µ—Ç–æ–¥ –æ–ø–ª–∞—Ç–∏</h2>
        <label>
          <input
            type="radio"
            value="cod"
            checked={paymentMethod === "cod"}
            onChange={() => setPaymentMethod("cod")}
          />
          –û–ø–ª–∞—Ç–∞ –ø—Ä–∏ –æ—Ç—Ä–∏–º–∞–Ω–Ω—ñ
        </label>
        <label>
          <input
            type="radio"
            value="card"
            checked={paymentMethod === "card"}
            onChange={() => setPaymentMethod("card")}
          />
          –û–ø–ª–∞—Ç–∞ –∫–∞—Ä—Ç–∫–æ—é –æ–Ω–ª–∞–π–Ω
        </label>
      </div>

      <div className={styles.links}>
        <Link to="/delivery-terms">üì¶ –£–º–æ–≤–∏ –¥–æ—Å—Ç–∞–≤–∫–∏</Link>
        <Link to="/return-policy">‚Ü© –ü–æ–ª—ñ—Ç–∏–∫–∞ –ø–æ–≤–µ—Ä–Ω–µ–Ω–Ω—è</Link>
      </div>

      <Button onClick={handleOrder} disabled={loading}>
        {loading ? "–í—ñ–¥–ø—Ä–∞–≤–∫–∞..." : "–í—ñ–¥–ø—Ä–∞–≤–∏—Ç–∏ –∑–∞–º–æ–≤–ª–µ–Ω–Ω—è"}
      </Button>
    </div>
  );
};

export default Checkout;
